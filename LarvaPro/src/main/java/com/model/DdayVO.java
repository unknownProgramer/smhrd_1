package com.model;

public class DdayVO {
	
	int D_day;
	int index;
	
	public DdayVO(int d_day, int index) {
		super();
		this.D_day = d_day;
		this.index = index;
	}

	public int getD_day() {
		return D_day;
	}

	public void setD_day(int d_day) {
		D_day = d_day;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
	
	

}
